// Helper function to get translated attraction
export const getTranslatedAttraction = (attraction, language = 'ru') => {
  if (!attraction || !attractionTranslations[attraction.id]) {
    return attraction;
  }

  const translations = attractionTranslations[attraction.id];
  
  return {
    ...attraction,
    name: translations.name[language] || attraction.name,
    description: translations.description[language] || attraction.description,
    longDescription: translations.longDescription[language] || attraction.longDescription,
    category: translations.category[language] || attraction.category,
  };
};

// Helper function to get all translated attractions
export const getTranslatedAttractions = (attractions, language = 'ru') => {
  return attractions.map(attraction => getTranslatedAttraction(attraction, language));
};
